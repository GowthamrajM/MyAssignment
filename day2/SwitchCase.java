package week1.day2;

public class SwitchCase {
	
	public static void main(String[] args) {
		
		String browser="edge";
		
		switch(browser){
		
		case "Chrome":
		System.out.println("Chrome");
		break;
		
		
		case "firefox":
		System.out.println("firefox");
		break;
		
		case "edge":
		System.out.println("edge");
		break;
		
		default:
			System.out.println("Mac");
		
	}
	}
}
